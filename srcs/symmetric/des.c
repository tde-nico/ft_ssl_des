void	des_init(u_int8_t *key, u_int32_t len)
{
	u_int32_t	i;
	u_int32_t	c;
	u_int32_t	d;
	u_int32_t	t;

	(void)len;
	ft_memset(&ctx, 0, sizeof(ctx));
	c = LOAD_D_BIG(key);
	d = LOAD_D_BIG(key + 4);

	KEY_PERM(c, d);

	i = -1;
	while (++i < 16)
	{
		if (i == 0 || i == 1 || i == 8 || i == 15) {
			c = ROL28(c, 1);
			d = ROL28(d, 1);
		} else {
			c = ROL28(c, 2);
			d = ROL28(d, 2);
		}

		ctx.keys[2 * i] = PERM_L(c, d);
		ctx.keys[2 * i + 1] = PERM_R(c, d);
	}
}

void	des_encrypt(u_int8_t *in, u_int8_t *out)
{
	u_int32_t	l;
	u_int32_t	r;
	u_int32_t	t;
	u_int32_t	i;

	l = LOAD_D_BIG(in);
	r = LOAD_D_BIG(in + 4);
	IP(l, r);
	i = 0;
	while (i < 32)
	{
		ROUND_SP(l, r, ctx.keys[i], ctx.keys[i + 1]);
		ROUND_SP(r, l, ctx.keys[i + 2], ctx.keys[i + 3]);
		i += 4;
	}
	IP_INV(r, l);
	STORE_D_BIG(r, out);
	STORE_D_BIG(l, out + 4);
}

void	des_decrypt(u_int8_t *in, u_int8_t *out)
{
	u_int32_t	l;
	u_int32_t	r;
	u_int32_t	t;
	u_int32_t	i;

	r = LOAD_D_BIG(in);
	l = LOAD_D_BIG(in + 4);
	IP(r, l);
	i = 32;
	while (i > 0)
	{
		ROUND_SP(r, l, ctx.keys[i - 2], ctx.keys[i - 1]);
		ROUND_SP(l, r, ctx.keys[i - 4], ctx.keys[i - 3]);
		i -= 4;
	}
	IP_INV(l, r);
	STORE_D_BIG(l, out);
	STORE_D_BIG(r, out + 4);
}
